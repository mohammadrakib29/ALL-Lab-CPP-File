/****
    12. C++ program to check whether a number is positive or negative

*/

#include <iostream>
#include <cmath>
using namespace std;

int main()
{
   int a;
   cin >> a;

   if(a>0)
   {
       cout << "Positive number:" << a;
   }
   else
    cout << "Negative number:" << a;

    return 0;
}

























